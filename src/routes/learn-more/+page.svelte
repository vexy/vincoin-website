<h1>What is VINCOIN ?</h1>

<p><code>Vincoin</code> is software implementation of unique monetary unit and homage to advanced <a href="https://en.wikipedia.org/wiki/Vin%C4%8Da_culture"> Vinčа culture</a> that, paradoxically, hadn’t used money at all !</p>
<p>The idea emerged back in 2018, at the dawn of cryptocurrency era and many faulty Bitcoin forks. Vincoin too is designed to be advanced variation of Bitcoin but with few marginal differences.</p>

<h2>Differences from Bitcoin</h2>
<p>At high level, Vincoin differs from Bitcoin in two major fields:</p>
<ul>
    <li><a href="/technical">Technical aspects</a></li>
    <li><a href="/economical">Economical aspects</a></li>
</ul>

<p>Vincoin will solely be built with post quantum cryptography algorithms.</p>
<p>Generally, Vincoin <u>will not be listed</u> on stock exchanges, sold separately nor be pegged to any other fiat currencies, but rather spread equally amongst its users.</p>
<p>Check the appropriate sections to learn about key differences in more depth.</p>

