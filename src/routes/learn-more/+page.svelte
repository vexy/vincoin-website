<h1>What is VINCOIN ?</h1>

<p>At the dawn of cryptocurrencies, back in 2018, the idea of Vincoin emerged.<br>
It is designed to be advanced variation of the Bitcoin but with several marginal differences.</p>

<h2>Mining philosophy</h2>
<p>Using block reward as "incentive" for people to start spreading Bitcoin network proved to be degenerative perception of the free and open sourced software.</p>
<p>Earning, selling and buying pieces of Bitcoin is actually <b>nowhere</b> to be found in <a href="https://bitcoin.org/bitcoin.pdf">original whitepaper</a>, technical documentation nor single line of code, so it can hardly be explained why most of the people assume Bitcoin actually have any market price. Specially one expressed in other fiat currencies... üòê</p>
<p>With that in mind, VINCOIN <u>does not award</u> node runners with any block reward(s), eliminating the possibility for similar destiny of Bitcoin "market offerings" and shallow perception.</p>

<h2>Advanced cryptography</h2>
<p>Compared to all the cryptography contained in Bitcoin, Vincoin took radically different approach.</p>
<p>Yes, it's born in 2018 so it had plenty of time to observe <a href="https://www.nist.gov/">NIST</a> efforts in formulating quantum resistant algorithms, which explains VINCOIN tendention to use only quantum resistant algorithms and nothing more.</p>
<p>With that huge difference in architecture, naturally many technical aspect of Vincoin are different from the beginning.</p>
<p>Above all, VINCOIN does apply some of the ZKP scheme, allowing it's users even more possibilities and use cases.</p>

<h2>Economical setup</h2>

<h3>Starting and total amount of VINCOINS</h3>
<p>As it is technically possible, VINCOIN is programmed to "award" any new wallet with starting sum of VINCOINS, untill all the VINCOINS are depleted.</p>
<p>That is made possible using different signing and cryptography algorithms and OPCODE commands.</p>
<p>Similarly to Bitcoin, VINCOIN as well has fixed amount of units of exchange, currently floating around 11-17 milions of coins.</p>
<p>Currently, Vincoin algorithms will award 1 <code>VIN</code> to every new wallet/user.</p>
<p>Advanced ZkP scheme will assure only one coin per wallet can be given.</p>

